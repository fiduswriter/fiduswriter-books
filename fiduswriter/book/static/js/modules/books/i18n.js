export function bookTerm(term, language) {
    if (!BOOK_TERMS[term]) {
        console.error(`Term not defined: ${term}`)
    }
    if (BOOK_TERMS[term][language].length) {
        return BOOK_TERMS[term][language]
    } else {
        return term
    }
}
